#2.7 Multiples

firstNum = int(input('\nEnter first numerator: '))
firstDenom = int(input('\nEnter first denominator: '))

if firstNum % firstDenom == 0 : 
   print('\n', firstNum, 'is a multiple of ', firstDenom)
else : 
   print('\n', firstNum, 'is not a multiple of ', firstDenom) 

secondNum = int(input('\nEnter second numerator: '))
secondDenom = int(input('\nEnter second denominator: '))

if secondNum % secondDenom == 0 : 
   print('\n', secondNum, 'is a multiple of ', secondDenom)
else : 
   print('\n', secondNum, 'is not a multiple of ', secondDenom) 



